#include <iostream>
using namespace std;
int main(){
    int n, square;
    
    // Asking for input
    cout << "Enter a number: ";
    cin >> n;
    
    // Calculating square
    square = n * n;
    
    // Displaying output
    cout << "Square of " << n << " is: " << square;
    
    return 0;
} 
  
